""" module3/are_of_the_circle.py

A program that accepts a value for the radius of a circle and computes the area of the circle.

"""
import math

print("are_of_the_circle.py.py\n")

inputRadius = float(input("Enter the radius of the circle: "))

print(f"The area of the circle is {math.pi * inputRadius ** 2}.")
